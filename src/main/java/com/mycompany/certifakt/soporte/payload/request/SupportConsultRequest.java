package com.mycompany.certifakt.soporte.payload.request;

public class SupportConsultRequest {
    
    private String rucEmisor;
    private String tipoComprobante;
    private String serie;
    private Integer numero;

    public SupportConsultRequest() {
    }

    public String getRucEmisor() {
        return rucEmisor;
    }

    public void setRucEmisor(String rucEmisor) {
        this.rucEmisor = rucEmisor;
    }

    public String getTipoComprobante() {
        return tipoComprobante;
    }

    public void setTipoComprobante(String tipoComprobante) {
        this.tipoComprobante = tipoComprobante;
    }

    public String getSerie() {
        return serie;
    }

    public void setSerie(String serie) {
        this.serie = serie;
    }

    public Integer getNumero() {
        return numero;
    }

    public void setNumero(Integer numero) {
        this.numero = numero;
    }

    @Override
    public String toString() {
        return "SupportConsultRequest{" + "rucEmisor=" + rucEmisor + ", tipoComprobante=" + tipoComprobante + ", serie=" + serie + ", numero=" + numero + '}';
    }
    
}
